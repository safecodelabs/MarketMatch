    /**
     * Format urban help provider details
     */
    formatUrbanHelpProvider(provider, language, index) {
        const lines = [];
        
        lines.push(`*${index + 1}. ${provider.name || 'Service Provider'}*`);
        
        if (provider.rating) {
            lines.push(`   â­ ${provider.rating}/5`);
        }
        
        if (provider.experience) {
            lines.push(`   ðŸ“… ${provider.experience} ${this.getMessage(language, 'experience') || 'experience'}`);
        }
        
        if (provider.contact) {
            lines.push(`   ðŸ“ž ${provider.contact}`);
        }
        
        if (provider.availability) {
            lines.push(`   ðŸ•’ ${provider.availability}`);
        }
        
        if (provider.rate) {
            lines.push(`   ðŸ’° ${provider.rate}`);
        }
        
        return lines.join('\n');
    }

